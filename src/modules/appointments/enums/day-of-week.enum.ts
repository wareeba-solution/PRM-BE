/**
 * Enum representing days of the week, aligned with JavaScript's Date.getDay() values
 * Sunday = 0, Monday = 1, etc.
 */
export enum DayOfWeek {
    SUNDAY = 0,
    MONDAY = 1,
    TUESDAY = 2,
    WEDNESDAY = 3,
    THURSDAY = 4,
    FRIDAY = 5,
    SATURDAY = 6
  }